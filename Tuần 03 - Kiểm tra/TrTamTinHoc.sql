-- hoặc nếu chỉ trả về NVARCHAR
CREATE FUNCTION fn_CauA_str (@MAGV VARCHAR(10))
RETURNS NVARCHAR(50)
	BEGIN
		DECLARE @TenKH NVARCHAR(50)
		SELECT TOP 1 @TenKH = TENKH
				FROM GIANG_DAY D, LOP_HOC L, KHOA_HOC K
				WHERE D.MALH = L.MALH
					AND L.MAKH = K.MAKH
					AND D.MAGV = @MAGV
		RETURN @TenKH
	END
SELECT dbo.fn_CauA_str('GV001')
