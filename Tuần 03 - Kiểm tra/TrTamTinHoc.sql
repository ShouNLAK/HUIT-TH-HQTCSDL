CREATE PROC sp_CauA @MAKHOA VARCHAR(10)
AS
	BEGIN
		SELECT *
		FROM GIAO_VIEN
		WHERE MAKHOA = @MAKHOA

		SELECT *
		FROM LOP_HOC L, GIANG_DAY D, GIAO_VIEN G
		WHERE L.MALH = D.MALH
			AND D.MAGV = G.MAGV
			AND MAKHOA = @MAKHOA
	END
EXEC sp_CauA 'CNTT'

