CREATE PROC sp_CauA @MANCC VARCHAR(10)
AS
	BEGIN
		SELECT M.MAMH, TENMH
		FROM MATHANG M, CTDH C, DATHANG D
		WHERE D.MANCC = @MANCC
			AND D.SODH = C.SODH
			AND C.MAMH = M.MAMH
	END
EXEC sp_CauA 'NCC002'
-- 2b
CREATE PROC sp_CauB @SODH VARCHAR(10)
AS
	BEGIN
		UPDATE DATHANG
		SET THANHTIEN = (	SELECT SUM( SLDAT * DGDAT )
							FROM CTDH
							WHERE SODH = @SODH	)
		WHERE SODH = @SODH
	END
EXEC sp_CauB 'DH002'
