CREATE TRIGGER tr_CauB ON CTDH
FOR INSERT, UPDATE
AS
	BEGIN
		DECLARE @SLD INT
		SELECT @SLD = SUM(SLDAT)
		FROM CTDH
		WHERE MAMH = (SELECT MAMH FROM inserted)

		IF (@SLD > (SELECT SLTON
					FROM INSERTED I, MATHANG M
					WHERE I.MAMH = M.MAMH) )
			BEGIN
				PRINT(N'Lỗi : Số lượng đặt phải nhỏ hơn hoặc bằng số lượng tồn')
				ROLLBACK TRAN
			END
		ELSE
			COMMIT TRAN
	END
