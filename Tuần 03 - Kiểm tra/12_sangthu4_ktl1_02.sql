CREATE FUNCTION fn_CauA (@THANG INT, @NAM INT)
RETURNS MONEY
	BEGIN
		DECLARE @Sum MONEY
		SELECT @Sum = Sum(THANHTIEN)
		FROM DATHANG
		WHERE MONTH(NGAYDH) = @THANG AND YEAR(NGAYDH) = @NAM
		RETURN @Sum
	END
SELECT dbo.fn_CauA(08,2024)
-- 3b
CREATE FUNCTION fn_CauB (@Tu DATE, @Den DATE)
RETURNS TABLE
	RETURN (	SELECT * 
				FROM MATHANG 
				WHERE MAMH NOT IN (	SELECT MAMH
									FROM CTDH C, DATHANG D
									WHERE C.SODH = D.SODH
										AND D.NGAYDH >= @Tu AND D.NGAYDH <= @Den)	)
SELECT * FROM fn_CauB('2025-09-01','2026-01-01')
