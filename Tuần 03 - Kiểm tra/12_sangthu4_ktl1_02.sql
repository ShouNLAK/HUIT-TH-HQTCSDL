)
CREATE TABLE CTDH
(
	SODH VARCHAR(10),
	MAMH VARCHAR(10),
	SLDAT INT,
	DGDAT MONEY,
	CONSTRAINT PK_CTDH PRIMARY KEY (SODH,MAMH),
	CONSTRAINT FK_CTDH_DATHANG FOREIGN KEY (SODH) REFERENCES DATHANG(SODH),
	CONSTRAINT FK_CTDH_MATHANG FOREIGN KEY (MAMH) REFERENCES MATHANG(MAMH)
)
