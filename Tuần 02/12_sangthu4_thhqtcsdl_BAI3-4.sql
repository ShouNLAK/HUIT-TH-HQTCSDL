CREATE PROC sp_KTSLMuonSach @MADG VARCHAR(10)
AS
	BEGIN
		SELECT COUNT(*)
		FROM MUONSACH
		WHERE MADG = @MADG AND NGAYTRA IS NULL
	END
EXEC sp_KTSLMuonSach 'DG002'
