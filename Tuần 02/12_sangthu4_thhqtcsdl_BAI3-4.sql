CREATE PROC sp_NgayMuonGanNhat @MASH VARCHAR(10)
AS
	BEGIN
		SELECT MAX(NGAYMUON)
		FROM MUONSACH
		WHERE MASH = @MASH
	END
EXEC sp_NgayMuonGanNhat 'S001'
