CREATE PROC sp_TTDocGia @MADG VARCHAR(10)
AS
	BEGIN
		SELECT TENDG, DIACHI
		FROM DOCGIA
		WHERE @MADG = MADG
	END
EXEC sp_TTDocGia 'DG001'
