CREATE PROC sp_KTAiMuon @MASH VARCHAR(10)
AS
	BEGIN
		SELECT TENDG
		FROM MUONSACH M, DOCGIA D
		WHERE M.MADG = D.MADG AND MASH = @MASH AND NGAYTRA IS NULL
	END
EXEC sp_KTAiMuon 'S001'
