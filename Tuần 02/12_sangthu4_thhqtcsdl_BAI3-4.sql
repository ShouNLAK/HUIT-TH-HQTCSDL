CREATE PROC sp_SoSachMuon @MADG VARCHAR(10), @NGAY DATE
AS
	BEGIN
		SELECT COUNT(*)
		FROM MUONSACH
		WHERE MADG = @MADG AND NGAYMUON = @NGAY
	END
EXEC sp_SoSachMuon 'DG001','2023-10-25'
