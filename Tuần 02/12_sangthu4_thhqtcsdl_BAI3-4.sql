CREATE FUNCTION f_XemTTDG (@MADG VARCHAR(10))
RETURNS NVARCHAR(300)
AS
	BEGIN
		DECLARE @TENDG NVARCHAR(100), @QUEQUAN NVARCHAR(200)
		SELECT @TENDG = TenDG, @QUEQUAN = DIACHI
		FROM DOCGIA
		WHERE MADG = @MADG
		RETURN @TENDG + N' có địa chỉ ở ' + @QueQuan
	END
SELECT DBO.f_XemTTDG('DG001')
