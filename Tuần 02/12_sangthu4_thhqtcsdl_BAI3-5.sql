CREATE PROC sp_CauC @MAHD VARCHAR(10)
AS
	BEGIN
		DECLARE @MAHG VARCHAR(10), @TENHG NVARCHAR(50), @SLBan INT, @GiaBan MONEY, @ThanhTien MONEY
		DECLARE cur_CauC cursor for (	SELECT H.MAHG, TENHG, SOLUONG, GIA, (SOLUONG * GIA)
										FROM CHITIETHD C, HANG H, DONGIA D
										WHERE C.MAHD = @MAHD 
											AND C.MAHG = H.MAHG AND H.MAHG = D.MAHG )
		OPEN cur_CauC
		PRINT N'Mã hàng' + Space(10) + N'Tên hàng' + Space(50) + N'số lượng' + Space(10) + N'Giá bán' + Space(20) + N'Thành tiền'
		FETCH NEXT FROM cur_CauC into @MAHG, @TENHG, @SLBAN, @GIABAN, @THANHTIEN
		WHILE(@@FETCH_STATUS = 0)
			BEGIN
				PRINT @MAHG + Space(10) + @TENHG + Space(50) + CONVERT(NVARCHAR(10),@SLBAN) + Space(10) + CONVERT(NVARCHAR(20),@GIABAN) + Space(20) + CONVERT(NVARCHAR(20),@THANHTIEN)
				FETCH NEXT FROM cur_CauC into @MAHG, @TENHG, @SLBAN, @GIABAN, @THANHTIEN
			END
		CLOSE cur_CauC
		DEALLOCATE cur_CauC
	END
EXEC sp_CauC 'HD001'