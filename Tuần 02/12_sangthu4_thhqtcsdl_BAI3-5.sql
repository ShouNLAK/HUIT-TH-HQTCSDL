CREATE PROC sp_PhanLoaiKhachHang @MAKH VARCHAR(10), @LOAIKH NVARCHAR(30) OUTPUT
AS
    BEGIN
        DECLARE @DOANHSO MONEY
        SELECT @DOANHSO = SUM(THANHTOAN)
        FROM HOADON
        WHERE MAKH = @MAKH

        IF @DOANHSO >= 10000000
            SET @LOAIKH = N'VIP'
        ELSE IF @DOANHSO >= 6000000 AND @DOANHSO < 10000000
            SET @LOAIKH = N'KH thành viên'
        ELSE
            SET @LOAIKH = N'KH thân thiết'
    END
DECLARE @Loai NVARCHAR(30)
EXEC sp_PhanLoaiKhachHang 'KH001', @Loai OUTPUT
PRINT N'Phân loại: ' + @Loai

