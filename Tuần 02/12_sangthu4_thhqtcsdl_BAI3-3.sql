CREATE PROC sp_DTrBinh_XLoai_SV @MASV VARCHAR(10)
AS
	BEGIN
		DECLARE @DTB FLOAT
		SELECT @DTB = (SUM(DIEMTHI*SOTC) / SUM(SOTC))
		FROM KETQUA K, MONHOC M
		WHERE MASV = @MASV AND K.MAMH = M.MAMH
		
		SELECT @DTB N'Điểm trung bình', CASE	WHEN @DTB >= 8 THEN N'Giỏi'
							WHEN @DTB >= 7 THEN N'Khá'	
							WHEN @DTB >= 5 THEN N'Trung bình'
							ELSE N'Yếu'
							END N'Xếp loại'
	END
EXEC sp_DTrBinh_XLoai_SV 'SV002'
