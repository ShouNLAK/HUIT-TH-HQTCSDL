				WHEN DIEMTB >= 7 AND DIEMTB < 8 THEN N'Khá'
				WHEN DIEMTB >= 8 THEN N'Giỏi'
			END
		WHERE MASV IN (SELECT MASV FROM inserted)
		   OR MASV IN (SELECT MASV FROM deleted)
