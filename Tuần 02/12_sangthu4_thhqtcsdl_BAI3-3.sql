CREATE PROC sp_KhenThuong   @MASV VARCHAR(10), @HOCKY INT
AS
    BEGIN
        DECLARE @DTB_HK FLOAT
        SELECT @DTB_HK = SUM(K.DIEMTHI * M.SOTC) / SUM(M.SOTC)
        FROM KETQUA K, MONHOC M
        WHERE K.MAMH = M.MAMH AND MASV = @MASV AND HOCKY = @HOCKY
        GROUP BY MASV

		DECLARE @KHENTHUONG NVARCHAR(20) = CASE WHEN @DTB_HK >=8 THEN N'Khen thưởng'
												ELSE N'Không khen thưởng'
												End
		SELECT @KHENTHUONG
    END
EXEC sp_KhenThuong 'SV001',1
