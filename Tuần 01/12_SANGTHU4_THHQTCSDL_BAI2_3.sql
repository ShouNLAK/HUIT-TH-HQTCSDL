CREATE VIEW CAUA
AS
	SELECT K.MAKH, TENKH, SUM(GIABAN * SOLUONG)
	FROM KHACH K, HOADON H, CHITIETHD CH
	WHERE K.MAKH = H.MAKH AND H.MAHD = CH.MAHD
	GROUP BY K.MAKH, TENKH

DROP VIEW CAUA
SELECT * FROM CAUA

CREATE VIEW CAUB
AS
	SELECT H.MAHD, TENKH, SUM(SOLUONG*GIABAN)
	FROM KHACH K, HOADON H, CHITIETHD CH
	WHERE K.MAKH = H.MAKH AND H.MAHD = CH.MAHD
	GROUP BY H.MAHD,TENKH

DROP VIEW CAUB
SELECT * FROM CAUB

CREATE VIEW CAUC
AS
	SELECT H.MAHG, TENHG, DVT, SUM(SOLUONG) N'Tổng số lượng'
	FROM HANG H, CHITIETHD CH
	WHERE H.MAHG = CH.MAHG
	GROUP BY H.MAHG, TENHG, DVT

DROP VIEW CAUC
SELECT * FROM CAUC

CREATE VIEW CAUD
AS
	SELECT H.MAHG, TENHG, DVT, SUM(SOLUONG) N'Tổng số lượng'
	FROM HANG H, CHITIETPN CP
	WHERE H.MAHG = CP.MAHG
	GROUP BY H.MAHG, TENHG, DVT

DROP VIEW CAUD
SELECT * FROM CAUD

CREATE VIEW CAUE
AS
	SELECT MONTH(NGAYLAP) N'Tháng' , SUM(SOLUONG *GIABAN) N'Tổng trị giá'
	FROM HOADON H, CHITIETHD CH
	WHERE H.MAHD = CH.MAHD
	GROUP BY MONTH(NGAYLAP)

DROP VIEW CAUE
SELECT * FROM CAUE

CREATE VIEW CAUF
AS
	SELECT MONTH(H.NGAYLAP) N'Tháng' , SUM((GIABAN - GIANHAP)*CH.SOLUONG) N'Tổng lợi nhuận'
	FROM HOADON H, CHITIETHD CH, CHITIETPN CP
	WHERE H.MAHD = CH.MAHD AND CH.MAHG = CP.MAHG
	GROUP BY MONTH(H.NGAYLAP)
DROP VIEW CAUF
SELECT * FROM CAUF

CREATE VIEW CAUG
AS
	SELECT MONTH(H.NGAYLAP) N'Tháng' ,SUM(CH.SOLUONG *GIABAN) N'Tổng trị giá', SUM((GIABAN - GIANHAP)*CH.SOLUONG) N'Tổng lợi nhuận'
	FROM HOADON H, CHITIETHD CH, CHITIETPN CP
	WHERE H.MAHD = CH.MAHD AND CH.MAHG = CP.MAHG
	GROUP BY MONTH(H.NGAYLAP)
DROP VIEW CAUG
SELECT * FROM CAUG

CREATE VIEW CAUH
AS
	SELECT H.MAHG, TENHG, DVT, SUM(CP.SOLUONG) N'Tổng số lượng nhập' , SUM(CH.SOLUONG) N'Tổng số lượng bán' , SUM(CP.SOLUONG - CH.SOLUONG) N'Số lượng còn lại'
	FROM HANG H, CHITIETPN CP, CHITIETHD CH
	WHERE H.MAHG = CP.MAHG AND H.MAHG = CH.MAHG
	GROUP BY H.MAHG, TENHG, DVT

DROP VIEW CAUH
SELECT * FROM CAUH
