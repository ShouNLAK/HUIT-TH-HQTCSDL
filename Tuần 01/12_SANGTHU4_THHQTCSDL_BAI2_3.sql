CREATE DATABASE QL_BanHang;

-- 1. Bảng KHACH
CREATE TABLE KHACH (
    MAKH CHAR(10) PRIMARY KEY,
    TENKH NVARCHAR(50),
    DIACHI NVARCHAR(100),
    DIENTHOAI VARCHAR(15)
);

-- 2. Bảng HANG
CREATE TABLE HANG (
    MAHG CHAR(10) PRIMARY KEY,
    TENHG NVARCHAR(100),
    DVT NVARCHAR(20),
    NHASX NVARCHAR(50) -- Thêm cột NHASX dựa theo ảnh dữ liệu
);

-- 3. Bảng HOADON
CREATE TABLE HOADON (
    MAHD CHAR(10) PRIMARY KEY,
    NGAYLAP DATE,
    MAKH CHAR(10),
    CONSTRAINT FK_HD_KH FOREIGN KEY (MAKH) REFERENCES KHACH(MAKH)
);

-- 4. Bảng CHITIETHD
CREATE TABLE CHITIETHD (
    MAHD CHAR(10),
    MAHG CHAR(10),
    SOLUONG INT,
    GIABAN MONEY,
    PRIMARY KEY (MAHD, MAHG),
    CONSTRAINT FK_CTHD_HD FOREIGN KEY (MAHD) REFERENCES HOADON(MAHD),
    CONSTRAINT FK_CTHD_HG FOREIGN KEY (MAHG) REFERENCES HANG(MAHG)
);

-- 5. Bảng PHIEUNHAP (Lưu ý: MANCC chưa có bảng gốc nên tạm để kiểu dữ liệu)
CREATE TABLE PHIEUNHAP (
    MAPN CHAR(10) PRIMARY KEY,
    NGAYLAP DATE,
    MANCC CHAR(10)
);

-- 6. Bảng CHITIETPN
CREATE TABLE CHITIETPN (
    MAPN CHAR(10),
    MAHG CHAR(10),
    SOLUONG INT,
    GIANHAP MONEY,
    PRIMARY KEY (MAPN, MAHG),
    CONSTRAINT FK_CTPN_PN FOREIGN KEY (MAPN) REFERENCES PHIEUNHAP(MAPN),
    CONSTRAINT FK_CTPN_HG FOREIGN KEY (MAHG) REFERENCES HANG(MAHG)
);
GO
