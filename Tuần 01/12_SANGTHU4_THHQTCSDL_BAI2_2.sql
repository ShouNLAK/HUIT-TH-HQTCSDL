USE QL_SINHVIEN
CREATE TABLE KHOA
(
	MAKHOA CHAR(10),
	TENKHOA NVARCHAR(50),
	CONSTRAINT PK_KHOA PRIMARY KEY (MAKHOA)
)
CREATE TABLE LOP
(
	MALOP CHAR(10),
	TENLOP NVARCHAR(50),
	SISODK INT,
	MAKHOA CHAR(10),
	CONSTRAINT PK_LOP PRIMARY KEY (MALOP),
	CONSTRAINT FK_LOP_KHOA FOREIGN KEY (MAKHOA) REFERENCES KHOA
)
CREATE TABLE SINHVIEN
(
	MASV CHAR(10),
	HOTEN NVARCHAR(50),
	NGSINH DATE,
	DCHI NVARCHAR(50),
	GIOITINH NVARCHAR(3),
	MALOP CHAR(10),
	CONSTRAINT PK_SINHVIEN PRIMARY KEY (MASV),
	CONSTRAINT FK_SINHVIEN_LOP FOREIGN KEY (MALOP) REFERENCES LOP
)
CREATE TABLE MONHOC
(
	MAMH CHAR(10),
	TENMH NVARCHAR(50),
	SOTC INT,
	CONSTRAINT PK_MONHOC PRIMARY KEY (MAMH)
)
CREATE TABLE KETQUA
(
	MASV CHAR(10),
	MAMH CHAR(10),
	DIEM FLOAT,
	CONSTRAINT PK_KETQUA PRIMARY KEY (MASV, MAMH),
	CONSTRAINT FK_KETQUA_SINHVIEN FOREIGN KEY (MASV) REFERENCES SINHVIEN,
	CONSTRAINT FK_KETQUA_MONHOC FOREIGN KEY (MAMH) REFERENCES MONHOC
)

