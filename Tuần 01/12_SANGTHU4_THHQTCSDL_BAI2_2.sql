SELECT MASV,HOTEN
FROM SINHVIEN SV, LOP L, KHOA K
WHERE SV.MALOP = L.MALOP AND L.MAKHOA = K.MAKHOA 
	AND	TENKHOA=N'Công nghệ thông tin'

SELECT KQ.MAMH, TENMH, DIEM
FROM MONHOC MH, KETQUA KQ
WHERE KQ.MASV = 'SV01' AND KQ.MAMH = MH.MAMH

SELECT KQ.MASV, SV.HOTEN, SV.NGSINH, L.TENLOP
FROM KETQUA KQ, SINHVIEN SV, LOP L
WHERE KQ.MAMH = 'M001' AND DIEM < 5
	AND SV.MALOP = L.MALOP AND SV.MASV = KQ.MASV

SELECT SV.MASV, HOTEN, SUM(DIEM * SOTC) / SUM(SOTC) N'Điểm trung bình tích lũy'
FROM SINHVIEN SV, KETQUA KQ,MONHOC MH
WHERE SV.MASV = KQ.MASV AND KQ.MAMH = MH.MAMH
GROUP BY SV.MASV, HOTEN

SELECT SV.MASV, HOTEN, SUM(SOTC) N'Số tín chỉ tích lũy'
FROM SINHVIEN SV, KETQUA KQ, MONHOC MH
WHERE SV.MASV = KQ.MASV AND KQ.MAMH = MH.MAMH
	AND KQ.DIEM >= 5
GROUP BY SV.MASV, HOTEN

SELECT MAMH, TENMH
FROM MONHOC
WHERE MAMH NOT IN (	SELECT DISTINCT MH.MAMH
					FROM MONHOC MH, KETQUA KQ
					WHERE MH.MAMH = KQ.MAMH)

SELECT DISTINCT SV.MALOP, TENLOP 
FROM LOP L, SINHVIEN SV
WHERE SV.MALOP = L.MALOP
	AND L.SISODK = (	SELECT Max(SISODK)
						FROM LOP     )

