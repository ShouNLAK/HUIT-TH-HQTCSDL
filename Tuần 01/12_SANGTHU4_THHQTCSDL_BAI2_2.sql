CREATE VIEW CAUA
AS
	SELECT K.MAKHOA, TENKHOA, SUM(SISODK) N'Tổng số sinh viên'
	FROM KHOA K, LOP L
	WHERE K.MAKHOA = L.MAKHOA
	GROUP BY K.MAKHOA, TENKHOA
DROP VIEW CAUA
SELECT * FROM CAUA

CREATE VIEW CAUB
AS
	SELECT MH.MAMH, TENMH, COUNT(*) N'Tổng số sinh viên'
	FROM MONHOC MH, KETQUA KQ
	WHERE KQ.MAMH = MH.MAMH
	GROUP BY MH.MAMH, TENMH
DROP VIEW CAUB
SELECT * FROM CAUB

CREATE VIEW CAUC
AS
	SELECT MH.MAMH, TENMH, SOTC, 
								(SELECT COUNT(*)
								FROM KETQUA KQ
								WHERE KQ.MAMH =MH.MAMH AND DIEM >=5) N'Số sinh viên đạt',
								(SELECT COUNT(*)
								FROM KETQUA KQ
								WHERE KQ.MAMH =MH.MAMH AND DIEM <5) N'Số sinh viên rớt'
	FROM MONHOC MH,KETQUA KQ
	WHERE MH.MAMH = KQ.MAMH
	GROUP BY MH.MAMH, TENMH, SOTC
DROP VIEW CAUC
SELECT * FROM CAUC

CREATE VIEW CAUD
AS
	SELECT MALOP, TENLOP, 
							(	SELECT COUNT(*)
								FROM SINHVIEN SV
								WHERE GIOITINH =N'Nam' AND SV.MALOP = L.MALOP) N'Số sinh viên Nam',
							(	SELECT COUNT(*)
								FROM SINHVIEN SV
								WHERE GIOITINH =N'Nữ' AND SV.MALOP = L.MALOP) N'Số sinh viên Nữ',
							SISODK N'Tổng số sinh viên'
	FROM LOP L
DROP VIEW CAUD
SELECT * FROM CAUD

CREATE VIEW CAUE
AS
	SELECT SV.MASV, HOTEN, SUM(SOTC) N'Số tín chỉ',
													(	SELECT SUM(SOTC) 
													FROM KETQUA KQ, MONHOC MH
													WHERE SV.MASV = KQ.MASV AND KQ.MAMH = MH.MAMH AND DIEM >= 5) N'Số tín chỉ tích lũy',
													(	SELECT SUM(DIEM * SOTC) / SUM (SOTC) 
													FROM KETQUA KQ, MONHOC MH
													WHERE SV.MASV = KQ.MASV AND KQ.MAMH = MH.MAMH) N'Điểm trung bình'
												
	FROM SINHVIEN SV,KETQUA KQ, MONHOC MH
	WHERE SV.MASV = KQ.MASV AND KQ.MAMH = MH.MAMH
	GROUP BY SV.MASV, HOTEN
